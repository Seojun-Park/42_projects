#include "libft.h"
#define MY_INT_MIN -2147483648

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		if (n == MY_INT_MIN)
		{
			ft_putchar('2');
			n = -147483648;
		}
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putchar((n % 10) + '0');
	}
	else
	{
		ft_putchar('0' + n);
	}
}
