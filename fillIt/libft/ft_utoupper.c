#include "libft.h"

char    *ft_utoupper(char *s)
{
    int i;

    i = -1;
    while (s[++i] != '\0')
    {
        if (s[i] >= 'a' && s[i] <= 'z')
            s[i] = ((s[i] - 'a') + 'A');
    }
    return (s);
}